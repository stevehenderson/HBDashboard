package hbdashboard;

import javax.swing.ImageIcon;
import javax.swing.JLabel;

/**
 *
 * @author henderso
 */
public class SLabel extends JLabel {
    
        ImageIcon img;
        ImageIcon img_a;
        ImageIcon img_e;    
        public boolean selected;        
        String keyword = "unset";        
        DashboardEventHandler deh;        
        public void select() {            
            selected=true;     
            deh.findKeyWord(keyword);
            setIcon(img_e);                    
        }
        public void mouseOut() {            
            if(!selected) setIcon(img);                    
        }
        
        public void mouseOver() {
           if(!selected)  setIcon(img_a);                    
        }
        
        public void reset() {
            selected=false;
            setIcon(img);                    
        }      
        
        public SLabel(String filenameNoExt, DashboardEventHandler aDeh, HBDashboardView app) {
            super();
            deh=aDeh;
            keyword = filenameNoExt;
            //Load the images
            img = new ImageIcon(HBDashboardApp2.class.getResource("resources/" + filenameNoExt + ".jpg"));        
            img_a = new ImageIcon(HBDashboardApp2.class.getResource("resources/" + filenameNoExt + "_a.jpg"));        
            img_e = new ImageIcon(HBDashboardApp2.class.getResource("resources/" + filenameNoExt + "_e.jpg"));                            
            setIcon(img);              
            addMouseListener(new ButtonLabelListener(this,app));
    }
}
